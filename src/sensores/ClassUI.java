    private ContextResponsesContainer getSalas() {
        NgsiRequest ng = new NgsiRequest();
        String bodyQuery = "{\n"
                + "	\"entities\":[\n"
                + "		{\n"
                + "			\"type\": \"Sala\",\n"
                + "			\"isPattern\": \"true\",\n"
                + "			\"id\": \"SalaInteligente.*\"\n"
                + "		}\n"
                + "	]\n"
                + "}";
        String resposta = ng.sendPost("/v1/queryContext", bodyQuery);
        ObjectMapper mapper = new ObjectMapper();

        try {
            return mapper.readValue(resposta, ContextResponsesContainer.class);
        } catch (IOException ex) {
            Logger.getLogger(ClassUI.class.getName()).log(Level.SEVERE, null, ex);
        }

        return null;
    }

    private String getSelectedButtonText(ButtonGroup buttonGroup) {
        for (Enumeration<AbstractButton> buttons = buttonGroup.getElements(); buttons.hasMoreElements();) {
            AbstractButton button = buttons.nextElement();
            if (button.isSelected()) {
                return button.getText();
            }
        }
        return null;
    }

    private int timeToInt(String time) {
        if (null != time) {
            switch (time) {
                case "13:00 - 14:00":
                    return 0;
                case "14:00 - 15:00":
                    return 1;
                case "15:00 - 16:00":
                    return 2;
                case "16:00 - 17:00":
                    return 3;
                default:
                    return -1;
            }
        }
        return -1;
    }

